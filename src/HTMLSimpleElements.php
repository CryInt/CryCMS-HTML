abstract class HTMLSimpleElements extends HTMLHelper
{
    protected static $simpleElements = [
        'abbr', 'address', 'article', 'aside', 'audio',
        'b', 'bdo', 'body', 'blockquote', 'button',
        'canvas', 'caption', 'cite', 'code', 'colgroup',
        'datalist', 'dd', 'del', 'details', 'dfn', 'div', 'dl', 'dt',
        'em',
        'fieldset', 'figcaption', 'figure', 'form', 'footer',
        'h1', 'h2', 'h3', 'h4', 'h5', 'h6', 'head', 'header', 'hgroup', 'html',
        'i', 'iframe', 'ins',
        'kbd',
        'label', 'legend', 'li', 'listing',
        'main', 'map', 'mark', 'meter',
        'nav', 'noscript',
        'object', 'ol', 'optgroup', 'output',
        'p', 'pre', 'progress',
        'q',
        'rp', 'rt', 'ruby',
        's', 'samp', 'section', 'select', 'small', 'span', 'script', 'strong', 'style', 'sub', 'summary', 'sup', 'svg',
        'table', 'tbody', 'td', 'tfoot', 'th', 'thead', 'tr', 'textarea', 'time', 'title',
        'u', 'ul',
        'var', 'video',
    ];

    protected static $halfElements = [
        'area',
        'base', 'br',
        'col',
        'embed',
        'hr',
        'img',
        'meta',
        'param',
        'source',
        'track',
        'wbr',
    ];

    public static function __callStatic($name, $arguments)
    {
        if (in_array($name, self::$simpleElements, true)) {
            return self::removeDoubleAfterTag(
                self::simpleElement(
                    $name,
                    $arguments[0] ?? '',
                    $arguments[1] ?? [],
                    $arguments[2] ?? false
                )
            );
        }

        if (in_array($name, self::$halfElements, true)) {
            return self::removeDoubleAfterTag(
                self::halfElement(
                    $name,
                    $arguments[0] ?? [],
                    $arguments[1] ?? false
                )
            );
        }

        return self::pre('HTML element "' . $name . '" is not exists');
    }

    protected static function simpleElement(
        string $element,
        string $content,
        array $properties = [],
        bool $withoutAfterTag = false
    ): string
    {
        $propertiesIn = self::generateProperties($properties);
        $content = in_array($element, HTML::$autoFramingContent) ? HTML::framingContent($content) : $content;
        $html = "<" . $element . $propertiesIn . ">" . $content . "</" . $element . ">";
        $html .= $withoutAfterTag ? '' : HTML::$afterTag;
        return self::generateAround($html, $properties);
    }

    protected static function halfElement(
        string $element,
        array $properties = [],
        bool $withoutAfterTag = false
    ): string
    {
        $propertiesIn = self::generateProperties($properties);
        $html = "<" . $element . $propertiesIn . ">";
        $html .= $withoutAfterTag ? '' : HTML::$afterTag;
        return self::generateAround($html, $properties);
    }
}